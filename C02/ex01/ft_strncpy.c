char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char *ans;

	ans = dest;
	while (n-- & *src)
	{
		*dest++ = *src++;
	}
	while (n-- + 1)
	{
		*dest++ = '\0';
	}
	return (ans);
}
