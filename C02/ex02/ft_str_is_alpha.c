/*
** {IS_UPPER} = [65 : 90]
** {IS_LOWER} = [97 : 122]
** {IS_ALPHA} = IS_LOWER + IS_UPPER
** {IS_NUM} = [48 : 57]
** {IS_PRINTABLE} = [32 : 126]
*/

int	ft_str_is_alpha(char *str)
{
	while (*str)
	{
		if ((*str < 65 | *str > 90) & (*str < 97 | *str > 122))
		{
			return (0);
		}
		str++;
	}
	return (1);
}
